# Add node_modules to assets and Sass load paths so imports like "bootstrap/..." resolve in production.
Rails.application.config.assets.paths << Rails.root.join('node_modules').to_s

begin
  if defined?(SassC) && SassC.respond_to?(:load_paths)
    SassC.load_paths << Rails.root.join('node_modules').to_s
  end
rescue StandardError
  # safe fallback â€” don't break boot if SassC isn't available
end
