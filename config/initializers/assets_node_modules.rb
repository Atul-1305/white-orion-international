# Add node_modules to assets and SassC load paths so imports like "bootstrap/scss/bootstrap"
# can be resolved by Sprockets/sassc-rails.
Rails.application.config.assets.paths << Rails.root.join('node_modules')

begin
  if defined?(SassC) && SassC.respond_to?(:load_paths)
    SassC.load_paths << Rails.root.join('node_modules').to_s
  end
rescue StandardError
  # keep initializer safe if SassC isn't available
end
